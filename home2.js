let botao = document.getElementById("b01");
let quadro = document.getElementById("q01");

botao.addEventListener('click', function() {
  if (quadro.style.display === 'block') {
    quadro.style.display = 'none';
  } else {
    quadro.style.display = 'block';
  }
});

